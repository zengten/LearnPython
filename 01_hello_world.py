# 3 - 2 = 1
print("3 - 2 =", 3 - 2)
# 3 x 2 = 6
print("3 x 2 =", 3 * 2)
# 3 / 2 = 1.5  区别其他语言取整
print("3 / 2 =", 3 / 2)
# 3 ^ 2 = 9  双乘号表示指数运算
print("3 ^ 2 =", 3 ** 2)
# 5 % 3 = 2  除法取余
print("5 % 3 =", 5 % 3)
# 5 // 3 = 1  除法取整
print("5 // 3 =", 5 // 3)


